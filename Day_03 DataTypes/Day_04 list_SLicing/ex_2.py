x = [10,100,1000,56.03972723,True,56+78j,"python",1000 ]
print(x)
print(type(x))

print("=================slicing =================")
print(x[::])  # [10, 100, 1000, 56.03972723, True, (56+78j), 'python', 1000]
print(x)      # [10, 100, 1000, 56.03972723, True, (56+78j), 'python', 1000]
print(x[0::])  # [10, 100, 1000, 56.03972723, True, (56+78j), 'python', 1000]
print(x[0::1]) # [10, 100, 1000, 56.03972723, True, (56+78j), 'python', 1000]
print(x*1)     # [10, 100, 1000, 56.03972723, True, (56+78j), 'python', 1000]
print(x[:])    # [10, 100, 1000, 56.03972723, True, (56+78j), 'python', 1000]
